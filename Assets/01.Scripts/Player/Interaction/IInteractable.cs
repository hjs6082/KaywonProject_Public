// 설명: 상호작용 가능한 오브젝트를 위한 인터페이스
// 용도: E키 등으로 상호작용할 수 있는 모든 오브젝트가 구현해야 함
// 사용법:
//   1. 상호작용할 오브젝트에 이 인터페이스를 구현하는 스크립트 추가
//   2. InteractionPrompt로 UI에 표시할 텍스트 제공
//   3. OnInteract()에서 상호작용 로직 구현
// =============================================================================

namespace GameDatabase.Player
{
    /// <summary>
    /// 상호작용 가능한 오브젝트 인터페이스
    /// E키 등으로 상호작용할 수 있는 오브젝트가 구현합니다.
    /// </summary>
    public interface IInteractable
    {
        /// <summary>
        /// 상호작용 프롬프트 텍스트
        /// 예: "문 열기", "아이템 줍기", "대화하기"
        /// UI에 "E키를 눌러 [프롬프트]" 형태로 표시됩니다.
        /// </summary>
        string InteractionPrompt { get; }

        /// <summary>
        /// 상호작용 가능 여부
        /// 조건에 따라 상호작용을 막을 수 있습니다.
        /// 예: 열쇠가 있어야 문을 열 수 있음
        /// </summary>
        bool CanInteract { get; }

        /// <summary>
        /// 상호작용 실행
        /// 플레이어가 상호작용 키를 눌렀을 때 호출됩니다.
        /// </summary>
        /// <param name="player">상호작용하는 플레이어</param>
        void OnInteract(PlayerController player);

        /// <summary>
        /// 포커스 시작 (선택사항)
        /// 플레이어가 이 오브젝트를 바라보기 시작할 때 호출됩니다.
        /// 하이라이트 효과 등에 사용할 수 있습니다.
        /// </summary>
        void OnFocusEnter();

        /// <summary>
        /// 포커스 종료 (선택사항)
        /// 플레이어가 더 이상 이 오브젝트를 바라보지 않을 때 호출됩니다.
        /// </summary>
        void OnFocusExit();
    }
}
