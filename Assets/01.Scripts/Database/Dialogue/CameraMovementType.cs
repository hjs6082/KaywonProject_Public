namespace GameDatabase.Dialogue
{
    /// <summary>
    /// 카메라 이동 방향 타입
    /// 대사 재생 중 카메라가 이동할 방향을 결정
    /// </summary>
    public enum CameraMovementType
    {
        None,           // 카메라 이동 없음 (정적 카메라)
        LeftToRight,    // 왼쪽에서 오른쪽으로 이동
        RightToLeft,    // 오른쪽에서 왼쪽으로 이동
        UpToDown,       // 위에서 아래로 이동
        DownToUp        // 아래에서 위로 이동
    }
}
